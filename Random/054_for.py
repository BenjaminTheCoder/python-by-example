import random
number = random.randint(1, 5)
won = False
turn = 0
while won == False and turn < 2:
    turn += 1
    num = int(input('enter a number between 1 and 5 '))
    if num == number:
        print('you win')
        won = True
    elif num > number:
        print('too high.')
    else:
        print('too low')

if won == False:
    print('you lose')


    

    


































































































































































































































































































































