import random
number = random.randint(1, 5)

num = int(input('enter a number between 1 and 5 '))
if num == number:
    print('you win')
elif num > number:
    print('too high.')
else:
    print('too low')
if num != number:
    num2 = int(input('enter another number. '))
if num2 != number:
    print('You lose!')
else:
    print('good job!')
    


    

    


































































































































































































































































































































