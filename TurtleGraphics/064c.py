import turtle

SIZE = 250
ANGLE = (360/5)# * 2
t = turtle.Pen()
t.turtlesize(1)
t.color('red')
t.shape('turtle')
t.speed(1)

t.forward(SIZE)
t.right(ANGLE)
t.forward(SIZE)
t.right(ANGLE)
t.forward(SIZE)
t.right(ANGLE)
t.forward(SIZE)
t.right(ANGLE)
t.forward(SIZE)

t.hideturtle()

